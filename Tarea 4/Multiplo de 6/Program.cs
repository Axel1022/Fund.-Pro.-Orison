//PROGRAMA QUE LEA UN NUMERO Y DETERMINE SI ES MÚLTIPLO DE 6.
using System;
{
    {
        int Num;
        Console.Write("Ingresa un numero: ");
        Num = int.Parse(Console.ReadLine());

        if (Num % 6 == 0)
        {
            Console.WriteLine(Num + " es multiplo de 6");
        }
        else
        {
            Console.WriteLine(Num + " no es multiplo de 6");
        }

    }
}

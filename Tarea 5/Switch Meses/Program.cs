/*Programa que le pida al usuario que inserte un numero
del 1 al 12. Aplicar un switch para que muestre el mes
correspondiente al número ingresado,siendo 1 = Enero y 12
=  Diciembre. Contemplar incluir un mensaje si el usuario
ingresa un numero fuera de este rango. */


string Num;
Console.Write("Ingrese un numero de mes. EJ : 1 = Enero, 12 = Diciembre :");
Num = Console.ReadLine();

switch (Num)
{
    case "1":
        Console.WriteLine("Mes elegido: Enero"); break;
    case "2":
        Console.WriteLine("Mes elegido: Febrero"); break;
    case "3":
        Console.WriteLine("Mes elegido: Marzo"); break;
    case "4":
        Console.WriteLine("Mes elegido: Abril"); break;
    case "5":
        Console.WriteLine("Mes elegido: Mayo"); break;
    case "6":
        Console.WriteLine("Mes elegido: Junio"); break;
    case "7":
        Console.WriteLine("Mes elegido: Julio"); break;
    case "8":
        Console.WriteLine("Mes elegido: Agosto"); break;
    case "9":
        Console.WriteLine("Mes elegido: Septiembre"); break;
    case "10":
        Console.WriteLine("Mes elegido: Octubre"); break;
    case "11":
        Console.WriteLine("Mes elegido: Noviembre"); break;
    case "12":
        Console.WriteLine("Mes elegido: Diciembre"); break;
    default:
        Console.WriteLine("No es una opcion valida."); break;

}


